#Ejercicio para saber si la suma de los numeros es par

a= int(input("Ingresa el primer numero entero"))
b= int(input("Ingresa el segundo numero entero"))

suma = a + b 

if suma % 2 == 0 :
    print("La suma de los numeros es par")

else:
    print("La suma de los numeros es impar") 
       
       
